public class Calculs {
   public static void main(String... args) {
 //Variable nombre qui est 7
    int nombre = 7;
    System.out.println (nombre);

 //Variable addition qui est 3
    int addition = 3;
    nombre += addition;
    System.out.println (nombre);

 //Variable multiplication qui est 5
    int multiplication = 5;
    System.out.println (nombre *= multiplication);

   }

}