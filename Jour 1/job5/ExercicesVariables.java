public class ExercicesVariables {
    public static void main(String[] args) {
        int num1 = 5;
        int num2 = 10;

        System.out.println("La valeur de num1 est " + num1 + " et la valeur de num2 est " + num2 + " .");

        num1 = 10;
        num2 = 5; 

        System.out.println("La valeur de num1 est " + num1 + " et la valeur de num2 est " + num2 + " .");
    }
}
